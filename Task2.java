package by.oksana.lesson2;

public class Task2 {
    public static void main (String [] args) {
        int c=0;

        c = 11 & 7;
        System.out.println("11 & 7= "+c);

        c = 15 | 12;
        System.out.println("15 | 12= "+c);

        c = 17 ^ 16;
        System.out.println("17 ^ 16= "+c);

        c = ~7;
        System.out.println("~7= "+c);

        c = ~18;
        System.out.println("~18= "+c);

        c = 22<<3;
        System.out.println("22<<3= "+c);

        c = 22>>3;
        System.out.println("22>>3=  "+c);

        c = 29>>>4;
        System.out.println("29>>>4= "+c);

    }
}
